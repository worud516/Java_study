package API2;

public class AutoBoxing_UnBoxing {
	public static void main(String[] args) {
		// Autoboxing
		Integer i = 10;
		Double d = 3.14;
		Float f = 3.14f;
		Character c = 'A';
		
		// Auto Unboxing
		int ii = i;
		double dd = d;
		float ff = f;
		char cc = c;
		System.out.println(ii);
		System.out.println(dd);
		System.out.println(ff);
		System.out.println(cc);
	}
}
