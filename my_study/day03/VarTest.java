package day03;

public class VarTest {
	public static void main(String[] args) {
		//정수형 변수
		int i=100;
		//실수형 변수
		float f=3.14f;
		double d= 3.14;
		//문자형 변수
		char c = 'c';
		//문자열 변수
		String s="hello";
		//논리형 변수
		boolean b= true;
		
		System.out.println(i);
		//Ctrl+Alt+화살표: 커서있는 코드 복사
		System.out.println(f);
		System.out.println(d);
		System.out.println(c);
		System.out.println(s);
		System.out.println(b);
	}

}
